#!/usr/bin/env node

import { chmodSync, mkdirSync, writeFileSync, existsSync } from 'node:fs'
import { resolve, join } from 'node:path'
import { createInterface } from 'node:readline/promises'
import { stdin as input, stdout as output } from 'node:process'

const DEFAULT_PACKAGE_MANAGER = process.env.BEFU_PACKAGE_MANAGER ?? 'bun@1.2.16'

/**
 * Convert input text into a safe folder/package slug.
 */
function toSlug(value) {
  return value
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9-\s]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
}

/**
 * Write JSON content with trailing newline.
 */
function writeJson(filePath, obj) {
  writeFileSync(filePath, `${JSON.stringify(obj, null, 2)}\n`)
}

/**
 * Write plain text content to file.
 */
function writeText(filePath, content) {
  writeFileSync(filePath, content)
}

/**
 * Parse supported CLI flags.
 */
function parseArgs(argv) {
  const parsed = {
    name: '',
    platform: '',
  }

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index]
    if (arg === '--name' && argv[index + 1]) {
      parsed.name = argv[index + 1]
      index += 1
      continue
    }

    if (arg === '--platform' && argv[index + 1]) {
      parsed.platform = argv[index + 1]
      index += 1
      continue
    }
  }

  return parsed
}

/**
 * Scaffold a minimal Befu workspace.
 */
async function main() {
  const args = parseArgs(process.argv.slice(2))
  const needsPrompt = args.name.length === 0 || args.platform.length === 0
  const rl = needsPrompt ? createInterface({ input, output }) : null

  try {
    console.log('create-befu-app v0.1.0')
    console.log('Scaffold a Befu app workspace.')

    const nameInput =
      args.name.length > 0 ? args.name : await rl.question('App name (default: my-befu-app): ')

    const appName = toSlug(nameInput || 'my-befu-app') || 'my-befu-app'

    const platformInput =
      args.platform.length > 0
        ? args.platform
        : await rl.question('Target platform [android/ios/both] (default: both): ')

    const platformAnswer = platformInput.trim().toLowerCase()
    const platform = ['android', 'ios', 'both'].includes(platformAnswer) ? platformAnswer : 'both'

    const projectDir = resolve(process.cwd(), appName)
    if (existsSync(projectDir)) {
      console.error(`Directory already exists: ${projectDir}`)
      process.exit(1)
    }

    mkdirSync(projectDir, { recursive: true })
    mkdirSync(join(projectDir, 'apps', 'web', 'src'), { recursive: true })
    mkdirSync(join(projectDir, 'packages', 'bridge', 'src'), { recursive: true })
    mkdirSync(join(projectDir, 'crates', 'core', 'src'), { recursive: true })
    mkdirSync(join(projectDir, 'scripts'), { recursive: true })

    writeJson(join(projectDir, 'package.json'), {
      name: appName,
      private: true,
      packageManager: DEFAULT_PACKAGE_MANAGER,
      workspaces: ['apps/*', 'packages/*'],
      scripts: {
        dev: 'bun run --cwd apps/web dev',
        build: 'bun run --cwd apps/web build',
        test: 'bun run --cwd packages/bridge test',
        quality: 'bun run test && bun run build',
        doctor: 'echo "Add project doctor checks"',
      },
    })

    writeText(
      join(projectDir, 'README.md'),
      `# ${appName}\n\nGenerated by create-befu-app.\n\n## Start\n\n\`\`\`bash\nbun install\nbun run dev\n\`\`\`\n\n## Platform\n\nTarget: ${platform}\n\n`,
    )

    writeText(
      join(projectDir, 'apps', 'web', 'src', 'main.ts'),
      `console.log('Welcome to ${appName}')\n`,
    )

    writeText(
      join(projectDir, 'packages', 'bridge', 'src', 'index.ts'),
      "export const ping = () => ({ pong: 'pong' as const })\n",
    )

    writeText(
      join(projectDir, 'crates', 'core', 'src', 'lib.rs'),
      'pub fn ping() -> &\'static str {\n    "pong"\n}\n',
    )

    writeText(
      join(projectDir, 'scripts', 'bootstrap.sh'),
      '#!/usr/bin/env bash\nset -euo pipefail\nbun install\n',
    )
    chmodSync(join(projectDir, 'scripts', 'bootstrap.sh'), 0o755)

    console.log('')
    console.log(`Created ${appName} at ${projectDir}`)
    console.log('Next steps:')
    console.log(`  cd ${appName}`)
    console.log('  bun install')
    console.log('  bun run dev')
  } finally {
    rl?.close()
  }
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
